#include <iostream>
using namespace std;

int main(void) {
    // Desde i igual a 1 hasta 6 avanzaremos de 1 en 1.
    for (int i = 1; i <= 6; i++) {
        cout << i << " al cuadrado es " << i * i << endl;
    }
    // Salida:
    // 1 al cuadrado es 1
    // 2 al cuadrado es 4
    // 3 al cuadrado es 9
    // 4 al cuadrado es 16
    // 5 al cuadrado es 25
    // 6 al cuadrado es 36
    return 0;
}